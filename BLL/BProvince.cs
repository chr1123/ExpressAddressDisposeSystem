		/// <summary>
		/// 获得数据列表
		/// </summary>
		public List<Model.Province> DataTableToList(DataTable dt)
		{
			List<Model.Province> modelList = new List<Model.Province>();
			int rowsCount = dt.Rows.Count;
			if (rowsCount > 0)
			{
				Model.Province model;
				for (int n = 0; n < rowsCount; n++)
				{
					model = dal.DataRowToModel(dt.Rows[n]);
					if (model != null)
					{
						modelList.Add(model);
					}
				}
			}
			return modelList;
		}

